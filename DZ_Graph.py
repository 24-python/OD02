import random  # Импортируем модуль для работы со случайными числами


class Graph():
    def __init__(self):
        # Инициализируем граф как пустой словарь
        # Ключи - города, значения - списки смежных городов
        self.graph = {}

    def addEdge(self, u, v):
        """
        Добавляем ребро между вершинами u и v
        Если вершины u нет в графе - создаем запись
        """
        if u not in self.graph:
            self.graph[u] = []  # Создаем пустой список смежности для нового города
        self.graph[u].append(v)  # Добавляем город v в список смежности города u

    def print_Graph(self):
        """
        Выводим граф в удобочитаемом формате:
        Город -> Связанный_город1 -> Связанный_город2 ...
        """
        for node in self.graph:
            print(node, "->", " -> ".join(map(str, self.graph[node])))


# Список крупных городов России
russian_cities = [
    "Москва", "Санкт-Петербург", "Новосибирск", "Екатеринбург", "Казань",
    "Нижний Новгород", "Челябинск", "Самара", "Омск", "Ростов-на-Дону",
    "Уфа", "Красноярск", "Пермь", "Воронеж", "Волгоград"
]

# Создаем экземпляр графа
g = Graph()

# Добавляем случайные связи между городами
# Для демонстрации создадим 10 случайных связей
for _ in range(10):
    # Выбираем два случайных города
    city1 = random.choice(russian_cities)
    city2 = random.choice(russian_cities)

    # Добавляем связь между ними (может быть направленной)
    g.addEdge(city1, city2)

# Выводим получившийся граф
print("Граф транспортных связей между городами России:")
g.print_Graph()